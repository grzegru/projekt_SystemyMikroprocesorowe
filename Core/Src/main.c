typedef struct{
	float  Kp;
	float  Ki;
	float  Kd;
	float  dt;
}pid_parameters_t;

typedef struct{
	pid_parameters_t p;
	float  previous_error, previous_integral;
}pid_t2;

float  calculate_discrete_pid(pid_t2* pid, float  setpoint, float  measured){
	float  u=0, P, I, D, error, integral, derivative;

	error = setpoint-measured;

	//proportional part
	P = pid->p.Kp * error;

	//integral part
	integral = pid->previous_integral + (error+pid->previous_error) ; //numerical integrator without anti-windup
	pid->previous_integral = integral;
	I = pid->p.Ki*integral*(pid->p.dt/2.0);

	//derivative part
	derivative = (error- pid->previous_error)/pid->p.dt;
	pid->previous_error = error;
	D = pid->p.Kd*derivative;

	//sum of all parts
	u = P + I + D; //without saturation

	if(P  + I + D<0){
		u = 0;
	}
	if(P  + I + D>999){
		u = 999;
	}
	return u;
}


